int		ft_add(int num1, int num2)
{
	return (num1 + num2);
}

int		ft_sub(int num1, int num2)
{
	return (num1 - num2);
}

int		ft_mul(int num1, int num2)
{
	return (num1 * num2);
}

int		ft_div(int num1, int num2)
{
	return (num1 / num2);
}

int		ft_mod(int num1, int num2)
{
	return (num1 % num2);
}
