int		ft_sqrt(int nb)
{
	int i;
	int root;

	i = (nb + 1) / 2;
	root = 0;
	while (i > 0)
	{
		if (nb % i == 0 && nb / i == i)
		{
			root = i;
		}
		i--;
	}
	return (root);
}
